import numpy as np
vector = np.array([1, 2, 3])

matrix = np.array([[4, 5, 6],
                   [7, 8, 9],
                   [10, 11, 12]])

result = vector * matrix

print("Vector:")
print(vector)

print("\nMatrix:")
print(matrix)

print("\nResult of Element-wise Multiplication:")
print(result)